package 배열기본;

public class 배열생성 {

	public static void main(String[] a) {
		// 배열(array) : 늘어놓다. 진열하다.
		// new: 복사
		int[] s = new int[1000];//int[] => int배열
		System.out.println(s);//주소
		System.out.println(s.length);
		s[0] = 100;
		System.out.println(s[0]);
		s[999] = 999;
		System.out.println(s[999]);
		s[555] = 1; //예매됨.
		
		//변수에 들어갈 수 있는 것
		//값: 기본형 데이터(정수, 실수, 문자, 논리)
		//   => 기본형 변수
		//주소 : 참조형 데이터(나머지 다, 배열, 클래스, ....)
		//   => 참조형 변수
		     
		
		
		
		
		
		
		
		
		
		
	}

}
